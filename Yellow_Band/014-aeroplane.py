import turtle
t=turtle.Turtle()
t.speed(5)

#bg color
t.penup()
t.backward(100)
t.pendown()
t.color('teal','teal')
t.begin_fill()
for i in range(4):
    t.forward(200)
    t.left(90)
t.end_fill()

#Drawing aeroplane
t.penup()
t.forward(100)
t.left(90)
t.forward(30)
t.pendown()

t.color("white")
t.left(120)
t.forward(25)
t.right(120)
t.forward(20)
t.right(60)
t.forward(10)
t.left(60)
t.forward(50)
t.left(110)
t.forward(80)
t.right(110)
t.forward(20)
t.right(60)
t.forward(85)
t.left(60)
t.forward(50)
for i in range(36):
    t.forward(1)
    t.right(5)

t.forward(50)
t.left(60)
t.forward(89)
t.right(70)
