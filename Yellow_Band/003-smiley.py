import turtle
t=turtle.Turtle()
t.screen.colormode(255)
t.color((255,219,172))
t.begin_fill()
t.circle(100)
t.end_fill()
t.penup()
t.goto(-30,120)
t.pendown()
t.color("blue")
t.begin_fill()
t.circle(20)
t.end_fill()
t.penup()
t.goto(30,120)
t.pendown()
t.color("blue")
t.begin_fill()
t.circle(20)
t.end_fill()
t.penup()
t.goto(-40,80)
t.pendown()
t.right(90)
t.circle(40,180)
turtle.done()