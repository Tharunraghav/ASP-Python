#append
print("append:")
authors=["Charles Dickens","Jane Austen","George Orwell","William Shakespeare"]
authors.append("Roald Dahl")
print(authors)
print("-------------------------------------------------------------------------------------------------------------------------------------------------")


#extend
print("extend:")
new_authors= ["John Milton","JRR Tolkien","Lewis Carroll"]
authors.extend(new_authors)
print(authors)
print("-------------------------------------------------------------------------------------------------------------------------------------------------")

#insert
print("insert:")
authors.insert(1,'George Orwell')
print(authors)
print("-------------------------------------------------------------------------------------------------------------------------------------------------")

#remove element
print("remove element:")
authors.remove("JRR Tolkien")
print(authors)
print("-------------------------------------------------------------------------------------------------------------------------------------------------")

#pop
print("pop:")
authors.pop(1)
print(authors)
print("-------------------------------------------------------------------------------------------------------------------------------------------------")


#find index value
print("index value:")
a=authors.index("George Orwell")
print(a)
print("-------------------------------------------------------------------------------------------------------------------------------------------------")


#count
print("count:")
c=authors.count("jane Austen")
print(c)
print("reverse:")
authors.reverse()
print(authors)
print("-------------------------------------------------------------------------------------------------------------------------------------------------")


print("sort:")
numbers=[3,2,4,5,8,9,6,1]
numbers.sort()
print(numbers)
print("-------------------------------------------------------------------------------------------------------------------------------------------------")

#copy
print("copy:")
copied=numbers.copy()
print(copied)
print("-------------------------------------------------------------------------------------------------------------------------------------------------")

print("Iterate through list:")
for i in authors:
    print(i)
print("-------------------------------------------------------------------------------------------------------------------------------------------------")

print("using Enumuration:")
for index,author in enumerate(authors):
    print(f"Index{index}: {author}")
print("-------------------------------------------------------------------------------------------------------------------------------------------------")

print("while loop with list:")
count=0
while count<=len(authors):
    print(authors[count])
    count=count+1
print("-------------------------------------------------------------------------------------------------------------------------------------------------")





